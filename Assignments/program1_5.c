#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
// Function Name    : Accept 
// Input            : integer 
// Output           : integer
// Description      : Accept one number and check whether it is divisible by 5 or not.
// Author           : Sahil Jagdish Lolage 
// Date             : 22/04/2023
//
////////////////////////////////////////////////////////////////////////////////

void Accept(int iNo)
{
    int iCnt = 0;

    for(iCnt; iCnt<= iNo ; iCnt++)
    {
        printf("*");
    }

}

////////////////////////////////////////////////////////////////////////////////
//Entry Point Function
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
        iValue = 5;

        Accept(iValue);
        return 0;
        

}