////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    int iCnt = 0;

    for(iCnt; iCnt<= iNo ; iCnt++)
    {
        printf("*");
    }
}

////////////////////////////////////////////////////////////////////////////////
//Entry Point Function
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
        
        printf("Enter number");
        scanf("%d",&iValue);

        Display(iValue);
        
        return 0;
        

}