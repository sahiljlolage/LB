#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
// Function Name    : Display 
// Input            : integer 
// Output           : integer
// Description      : Accept one number from user and print that number of * on screen.
// Author           : Sahil Jagdish Lolage 
// Date             : 22/04/2023
